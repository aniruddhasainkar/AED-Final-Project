public class HighCourtCase {
    String highCaseID;
    String highName;
    String highAge;
    String highEmail;
    String highPhone;
    String highCaseType;
    String highStatus;

    public String getHighCaseID() {
        return highCaseID;
    }

    public void setHighCaseID(String highCaseID) {
        this.highCaseID = highCaseID;
    }

    public String getHighName() {
        return highName;
    }

    public void setHighName(String highName) {
        this.highName = highName;
    }

    public String getHighAge() {
        return highAge;
    }

    public void setHighAge(String highAge) {
        this.highAge = highAge;
    }

    public String getHighEmail() {
        return highEmail;
    }

    public void setHighEmail(String highEmail) {
        this.highEmail = highEmail;
    }

    public String getHighPhone() {
        return highPhone;
    }

    public void setHighPhone(String highPhone) {
        this.highPhone = highPhone;
    }

    public String getHighCaseType() {
        return highCaseType;
    }

    public void setHighCaseType(String highCaseType) {
        this.highCaseType = highCaseType;
    }

    public String getHighStatus() {
        return highStatus;
    }

    public void setHighStatus(String highStatus) {
        this.highStatus = highStatus;
    }
}
