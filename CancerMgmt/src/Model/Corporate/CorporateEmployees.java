public class CorporateEmployees {
     String corpID;
    String corpName;
    String corpEmail;
    String corpPhone;
    String corpDesignation;
    String corpDepartment;

    public String getCorpID() {
        return corpID;
    }

    public void setCorpID(String corpID) {
        this.corpID = corpID;
    }

    public String getCorpName() {
        return corpName;
    }

    public void setCorpName(String corpName) {
        this.corpName = corpName;
    }

    public String getCorpEmail() {
        return corpEmail;
    }

    public void setCorpEmail(String corpEmail) {
        this.corpEmail = corpEmail;
    }

    public String getCorpPhone() {
        return corpPhone;
    }

    public void setCorpPhone(String corpPhone) {
        this.corpPhone = corpPhone;
    }

    public String getCorpDesignation() {
        return corpDesignation;
    }

    public void setCorpDesignation(String corpDesignation) {
        this.corpDesignation = corpDesignation;
    }

    public String getCorpDepartment() {
        return corpDepartment;
    }

    public void setCorpDepartment(String corpDepartment) {
        this.corpDepartment = corpDepartment;
    }

    
}
