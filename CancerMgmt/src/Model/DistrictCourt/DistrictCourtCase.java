package Model.DistrictCourt;

/**
 *
 * @author keerthanaakannan
 */
public class DistrictCourtCase {
    String districtCaseID;
    String districtName;
    String districtAge;
    String districtEmail;
    String districtPhone;
    String districtCaseType;
    String districtStatus;

    public String getDistrictCaseID() {
        return districtCaseID;
    }

    public void setDistrictCaseID(String districtCaseID) {
        this.districtCaseID = districtCaseID;
    }

    public String getDistrictName() {
        return districtName;
    }

    public void setDistrictName(String districtName) {
        this.districtName = districtName;
    }

    public String getDistrictAge() {
        return districtAge;
    }

    public void setDistrictAge(String districtAge) {
        this.districtAge = districtAge;
    }

    public String getDistrictEmail() {
        return districtEmail;
    }

    public void setDistrictEmail(String districtEmail) {
        this.districtEmail = districtEmail;
    }

    public String getDistrictPhone() {
        return districtPhone;
    }

    public void setDistrictPhone(String districtPhone) {
        this.districtPhone = districtPhone;
    }

    public String getDistrictCaseType() {
        return districtCaseType;
    }

    public void setDistrictCaseType(String districtCaseType) {
        this.districtCaseType = districtCaseType;
    }

    public String getDistrictStatus() {
        return districtStatus;
    }

    public void setDistrictStatus(String districtStatus) {
        this.districtStatus = districtStatus;
    }
}
