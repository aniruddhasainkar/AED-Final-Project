package Model.PrisonCell;

/**
 *
 * @author keerthanaakannan
 */
public class Prison {
    String prisonID;
    String prisonName;
    String prisonAge;
    String prisonEmail;
    String prisonPhone;
    String prisoncaseType;
    String prisonStatus;

    public String getPrisonID() {
        return prisonID;
    }

    public void setPrisonID(String prisonID) {
        this.prisonID = prisonID;
    }

    public String getPrisonName() {
        return prisonName;
    }

    public void setPrisonName(String prisonName) {
        this.prisonName = prisonName;
    }

    public String getPrisonAge() {
        return prisonAge;
    }

    public void setPrisonAge(String prisonAge) {
        this.prisonAge = prisonAge;
    }

    public String getPrisonEmail() {
        return prisonEmail;
    }

    public void setPrisonEmail(String prisonEmail) {
        this.prisonEmail = prisonEmail;
    }

    public String getPrisonPhone() {
        return prisonPhone;
    }

    public void setPrisonPhone(String prisonPhone) {
        this.prisonPhone = prisonPhone;
    }

    public String getPrisoncaseType() {
        return prisoncaseType;
    }

    public void setPrisoncaseType(String prisoncaseType) {
        this.prisoncaseType = prisoncaseType;
    }

    public String getPrisonStatus() {
        return prisonStatus;
    }

    public void setPrisonStatus(String prisonStatus) {
        this.prisonStatus = prisonStatus;
    }
}
