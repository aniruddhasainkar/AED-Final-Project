public class SupremeCourtCase {
    String supremeCaseID;
    String supremeName;
    String supremeAge;
    String supremeEmail;
    String supremePhone;
    String supremeCaseType;
    String supremeStatus;

    public String getSupremeCaseID() {
        return supremeCaseID;
    }

    public void setSupremeCaseID(String supremeCaseID) {
        this.supremeCaseID = supremeCaseID;
    }

    public String getSupremeName() {
        return supremeName;
    }

    public void setSupremeName(String supremeName) {
        this.supremeName = supremeName;
    }

    public String getSupremeAge() {
        return supremeAge;
    }

    public void setSupremeAge(String supremeAge) {
        this.supremeAge = supremeAge;
    }

    public String getSupremeEmail() {
        return supremeEmail;
    }

    public void setSupremeEmail(String supremeEmail) {
        this.supremeEmail = supremeEmail;
    }

    public String getSupremePhone() {
        return supremePhone;
    }

    public void setSupremePhone(String supremePhone) {
        this.supremePhone = supremePhone;
    }

    public String getSupremeCaseType() {
        return supremeCaseType;
    }

    public void setSupremeCaseType(String supremeCaseType) {
        this.supremeCaseType = supremeCaseType;
    }

    public String getSupremeStatus() {
        return supremeStatus;
    }

    public void setSupremeStatus(String supremeStatus) {
        this.supremeStatus = supremeStatus;
    }
}
