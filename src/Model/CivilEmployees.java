public class CivilEmployees {
    String civilID;
    String civilName;
    String civilEmail;
    String civilPhone;
    String civilDesignation;
    String civilDepartment;

    public String getCivilID() {
        return civilID;
    }

    public void setCivilID(String civilID) {
        this.civilID = civilID;
    }

    public String getCivilName() {
        return civilName;
    }

    public void setCivilName(String civilName) {
        this.civilName = civilName;
    }

    public String getCivilEmail() {
        return civilEmail;
    }

    public void setCivilEmail(String civilEmail) {
        this.civilEmail = civilEmail;
    }

    public String getCivilPhone() {
        return civilPhone;
    }

    public void setCivilPhone(String civilPhone) {
        this.civilPhone = civilPhone;
    }

    public String getCivilDesignation() {
        return civilDesignation;
    }

    public void setCivilDesignation(String civilDesignation) {
        this.civilDesignation = civilDesignation;
    }

    public String getCivilDepartment() {
        return civilDepartment;
    }

    public void setCivilDepartment(String civilDepartment) {
        this.civilDepartment = civilDepartment;
    }
    
}
