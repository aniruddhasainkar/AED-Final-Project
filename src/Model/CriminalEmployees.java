package Model;

/**
 *
 * @author aniruddhasainkar
 */
public class CriminalEmployees {
    String criminalID;
    String criminalName;
    String criminalEmail;
    String criminalPhone;
    String criminalDesignation;
    String criminalDepartment;

    public String getCriminalID() {
        return criminalID;
    }

    public void setCriminalID(String criminalID) {
        this.criminalID = criminalID;
    }

    public String getCriminalName() {
        return criminalName;
    }

    public void setCriminalName(String criminalName) {
        this.criminalName = criminalName;
    }

    public String getCriminalEmail() {
        return criminalEmail;
    }

    public void setCriminalEmail(String criminalEmail) {
        this.criminalEmail = criminalEmail;
    }

    public String getCriminalPhone() {
        return criminalPhone;
    }

    public void setCriminalPhone(String criminalPhone) {
        this.criminalPhone = criminalPhone;
    }

    public String getCriminalDesignation() {
        return criminalDesignation;
    }

    public void setCriminalDesignation(String criminalDesignation) {
        this.criminalDesignation = criminalDesignation;
    }

    public String getCriminalDepartment() {
        return criminalDepartment;
    }

    public void setCriminalDepartment(String criminalDepartment) {
        this.criminalDepartment = criminalDepartment;
    }
}
